            <hr>
            <p>
                Micro-Merchant Asia is a data analytics and information hub about small merchants specializing in the trade of FMCG products with annual turnover of under 0.3 million BDT.
                It works as an integrator of data-driven efforts towards this large sub sector of distributive retail markets in the region. The platform starts out initially offering the following:
                <br>
                <img src="{{asset('project/frontend/images/img/articles/platform.png')}}" class="img-responsive"/>
                <br>
                Gradually, the platform will engage state and non-state actors from other south Asian countries to open up data sets and information on respective Micro Merchant communities. 
                Collaborating with a wide swathe of country-level and regional bodies and institutions interested in Micro Merchants,
                Micro Merchant Asia will open up data sets and propagate insights for data-driven policy making, innovations and investment in the retail distributive sub sector.
            </p>
        </div>
    </div>    
    <div id="showDetailsSliderArticles_2" class="col-lg-12 col-md-12 col-sm-12 showDetailsSliderArticles_style">
        <div class="slider_text_details_container">
            <h1>Why It is Here</h1>
            <hr>
            <ul>
                <li>
                    <p>
                        <strong>Wide Data Gaps on Micromerchants: &nbsp;</strong>
                        There is inadequate evidence and absence of reliable data and market information about Micromerchants in the region. Such lack of complete and accessible data dampens public policy and private sector investments thereby undermining the economic potential of this large community. Data gaps are even more severe for women owned and run retail businesses in rural backwaters of the region. More data of high quality about Micromerchants will translate into better prospects for the sector.
                    </p>
                </li>
                <li>
                    <p>
                        <strong>Low Knowledge & Skills of Micromerchants: &nbsp;</strong>
                    Among other constraints, Micromerchants are constrained by lack of entrepreneurial, managerial and technical skills which significantly limit growth potential.  The problem of adequate skills varies according to the sizes of enterprises suggesting that lack of knowledge and skills is particularly acute in smaller and micro size businesses. Making appropriate training materials available for Micromerchants can help them overcome critical skill deficits at their own pace.    
                    </p>
                </li>
                <li>
                    <p>
                        <strong>Inadequate Business Development Services for Micromerchants: &nbsp;</strong>
                    There is also a lack of reliable and up-to-date information on the sector of business development services for MSMEs in general. This, coupled with the lack of understanding of Micromerchant reality, impedes the development of appropriate capacity development solutions. Proper identification, mapping and profiling of such services can enable Micromerchants to build core skills and create linkages with larger value chains.      
                    </p>
                </li>
            </ul>
        </div>
    </div>

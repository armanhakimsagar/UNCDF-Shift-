        <div class="slider_text_details_container">
            <h1>How It Helps?</h1>
            <p>
                The platform thrives in the spirit of open data and collaboration with the objective of facilitating data-driven decision-making for businesses and policy-makers. It aims to achieve the objective by helping:
                <br>
                <img src="{{asset('project/frontend/images/img/articles/platform_how.png')}}" width="80%"  class="img-responsive"/>
                <br>
                Building on the initial set of data and insights from the “Shaping Inclusive Finance Transformations in SAARC: Merchants Development Driving Rural Markets in Bangladesh (SHIFT-MDDRM)” project, the platform will assemble more data from various sources, present transformative insights by incorporating more visualization panels and engage relevant actors. Gradually, both open and proprietary data sets pertaining to the retail sub-sector of all south Asian countries will be processed, indexed, streamlined and visualized for impact. 
            </p>
        </div>
    </div>
